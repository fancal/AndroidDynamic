    public static ClassLoader installPatch(Context context, File patch) {
        File dexDir = context.getDir("app_dex", Context.MODE_PRIVATE);
//        File dexDir = new File(context.getDir(), DROID_CODE_CACHE);
        List<File> files = new ArrayList<File>();
        files.add(patch);

        ClassLoader classLoader = context.getClassLoader();

        try {
            installDex(classLoader, dexDir, files, true);
        } catch (IllegalAccessException e) {
            e.printStackTrace();
        } catch (NoSuchFieldException e) {
            e.printStackTrace();
        } catch (InvocationTargetException e) {
            e.printStackTrace();
        } catch (NoSuchMethodException e) {
            e.printStackTrace();
        } catch (IOException e) {
            e.printStackTrace();
        } catch (InstantiationException e) {
            e.printStackTrace();
        }

        return classLoader;
    }

    public static void copyFile(File source, File dest)

            throws IOException {

        InputStream input = null;

        OutputStream output = null;
        if (!dest.exists()) {
            dest.createNewFile();
        } else {
            return;
        }
        try {

            input = new FileInputStream(source);

            output = new FileOutputStream(dest);

            byte[] buf = new byte[1024];

            int bytesRead;

            while ((bytesRead = input.read(buf)) > 0) {

                output.write(buf, 0, bytesRead);

            }

        } finally {
            if (input != null)
                input.close();
            if (output != null)
                output.close();

        }

    }

